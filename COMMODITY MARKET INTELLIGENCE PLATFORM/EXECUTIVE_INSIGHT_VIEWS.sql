USE WAREHOUSE COMMODITY_WH;
USE DATABASE COMMODITY_MARKET_PLATFORM;

-- View 1: Gold market summary by year

CREATE OR REPLACE VIEW ANALYTICS.VW_GOLD_ANNUAL_SUMMARY AS
WITH yearly_bounds AS (
  SELECT
    YEAR,
    FIRST_VALUE(CLOSE_PRICE) OVER (PARTITION BY YEAR ORDER BY TRADE_DATE) AS YEAR_OPEN,
    LAST_VALUE(CLOSE_PRICE) OVER (PARTITION BY YEAR ORDER BY TRADE_DATE
      ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING) AS YEAR_CLOSE,
    CLOSE_PRICE,
    TRADE_VOLUME,
    TRADE_DATE
  FROM ANALYTICS.FACT_GOLD_MARKET
)
SELECT
  YEAR,
  COUNT(*) AS TRADING_DAYS,
  ROUND(MIN(CLOSE_PRICE), 2) AS YEAR_LOW,
  ROUND(MAX(CLOSE_PRICE), 2) AS YEAR_HIGH,
  ROUND(AVG(CLOSE_PRICE), 2) AS YEAR_AVG,
  ROUND(SUM(TRADE_VOLUME), 0) AS TOTAL_VOLUME,
  ROUND((MAX(YEAR_CLOSE) - MAX(YEAR_OPEN)) / NULLIF(MAX(YEAR_OPEN), 0) * 100, 2) AS ANNUAL_RETURN_PCT
FROM yearly_bounds
GROUP BY YEAR
ORDER BY YEAR;

